//从任意位置开始显示一张任意大小的图片 （但是注意图片显示的起始位置和结束位置不能超出屏幕范围）
int bmp2lcd(const char *bmpfile,int X, int Y)
{
	int filp;
	int x,y,i,ret;
	int width,height,bits;
	BMPFH  BmpFH;  	 //BMP文件信息头
	BMPIH  BmpIH;    //BMP位图信息头

	//加载一张BMP位图
	// 打开BMP，获得操作许可
	filp = open(bmpfile,O_RDONLY);
	if(filp == -1)
	{
		perror("open BMP");
		return -1;
	}	


	//获取BMP的文件信息头  14BYTE
	ret = read(filp,&BmpFH,sizeof(BMPFH));
	if(ret != sizeof(BMPFH))
	{
		perror("read Failed");
	}
	printf("TYPE: %c",BmpFH.bfType & 0xFF);     //显示bfType的低字节
	printf("%c \n",(BmpFH.bfType >> 8) & 0xFF); //显示bfType的高字节
	printf("FILE SIZE: %d \n",BmpFH.bfSize);
	//获取BMP的位图信息头
	read(filp,&BmpIH,sizeof(BMPIH));
	printf("BIT: %d \n",BmpIH.biBitCount);
	printf("RGB SIZE: %d \n",BmpIH.biSizeImage);	
	printf("x width: %d \n",BmpIH.biWidth);
	printf("x heigth: %d \n",BmpIH.biHeight);
	
	width   = BmpIH.biWidth;
	height  = BmpIH.biHeight;
	bits    = BmpIH.biBitCount>>3;  // 相当于 BmpIH.biBitCount/8 ，获得一个像素对应的字节数
	
	if((width + X)>800 || (height+Y)>480)
	{
		close(filp);//关闭文件
		free(bmp_buf);//释放malloc分配的空间
		printf("This BMP file is too large\n");
		return 	-1;
	}
	
	bmp_buf = (unsigned char *)malloc(width*height*bits);
	memset(bmp_buf,0,width*height*bits);//先清空这个分配的空间为0

	
	//获取BMP位图的RGB信息  从文件中读取800*480*3个字节
	read(filp,bmp_buf,width*height*bits);
	
	//将24bit的RGB填充到32bit的ARGB中
	for(y=(480-Y-height),i=0;y<(479-Y);y++)
		for(x=X;x<X+width;x++,i+=3)
			FB[(479-y)*800+x]  =  (bmp_buf[i] |  bmp_buf[i+1]<<8  |  bmp_buf[i+2] <<16);
	 // *(FB+(479-y)*800+x)   FB 作为显存的首地址，通过指针偏移操作，访问对应的像素位置
	  
	//写LCD
	//尺寸 7 寸   分辨率: 800*480 色深（位）： ARGB  16bit（565; 5:5:5:1）   24bit(8:8:8)  32bit(8:8:8:8)
	// 黑色： 0x00000000   白色： 0xFFFFFFFF  红色：0xFF0000
	close(filp);//关闭文件
	free(bmp_buf);//释放malloc分配的空间
	

	return 0;
 }
 

//X0:0~799  Y0:0~479
int show_bmpxy(char *pathname,int X0, int Y0)
{
	int x, y; //嵌套循环的计数值 行列
	int k;	//bmp_buf的循环计数值

	int fb0_fd;
	int bmp_fd;

	unsigned long *pfb0; //显存通过mmap映射到内存的首地址
	unsigned char *pbmp; //malloc分配给read图像数据的空间首地址指针

	unsigned int BMPwidth; //图像宽高度
	unsigned int BMPheight;

	//1、打开显存设备
	fb0_fd = open("/dev/fb0", O_RDWR);
	if (-1 == fb0_fd)
	{
		perror("open fb0 fail");
		return -1;
	}

	//2、映射显存空间到内存中  pfb0是其首地址
	pfb0 = (unsigned long *)mmap(NULL, 800 * 480 * 4, PROT_READ | PROT_WRITE, MAP_SHARED, fb0_fd, 0);
	if (MAP_FAILED == pfb0)
	{
		perror("mmap fail");
		return -1;
	}

	//2、打开图片文件  注意 图片文件名是从调用次函数的外面传入
	bmp_fd = open(pathname, O_RDONLY);
	if (-1 == bmp_fd)
	{
		perror("open bmp fail");
		return -1;
	}

	//3、跳过前面的18字节位图文件头  再读位图信息头的图像宽高
	lseek(bmp_fd, 18, SEEK_SET);
	read(bmp_fd, &BMPwidth, 4);
	read(bmp_fd, &BMPheight, 4);
	printf("x:%d\n", BMPwidth); //打印出读取的图片长宽
	printf("y:%d\n", BMPheight);

	//3、动态内存malloc分配空间  并读bmp 到这个分配的空间中   RGB

	lseek(bmp_fd, 54, SEEK_SET);
	pbmp = (unsigned char *)malloc(BMPwidth * BMPheight * 3);
	if (-1 == read(bmp_fd, pbmp, BMPwidth * BMPheight * 3)) //一次性读完800*480 bmp图片RGB信息
	{
		perror("read bmp fail");
		return -1;
	}

	//4、检测图片大小和位置不超标
	if (BMPheight + Y0 > 480 || BMPwidth + X0 > 800)
	{
		printf("bmp if too large!\n");
		return -1;
	}

	//5、较难理解 ！！！转换RGB --- ARGB --- 同时直接将转化的ARGB数据 "赋值" 到映射后的显存空间
	for (k = 0, y = Y0; y < BMPheight + Y0; y++)
	{
		for (x = X0; x < BMPwidth + X0; x++, k += 3)
		{
			//上下翻转        ARGB              BGR
			pfb0[(BMPheight + 2 * Y0 - y - 1) * 800 + x] = (pbmp[k] << 0) | (pbmp[k + 1] << 8) | (pbmp[k + 2] << 16);
		}
	}

	//6、显示完成  关闭相关文件和释放相关资源
	close(fb0_fd);
	close(bmp_fd);

	munmap(pfb0, 800 * 480 * 4); //解除映射 mmap
	free(pbmp);					 //释放malloc分配的堆空间

	return 0;
}

